# A função dir() retorna uma lista que contém todos os atributos e métodos disponíveis para um objeto.

# Exemplo:

programming_language = "Python"
print(dir(programming_language))  # Retorna todos os atributos e métodos disponíveis para 'programming_language'.

# A função help() apresenta a documentação disponível para classes, funções, objetos e etc.

# Exemplo:

help(str)  # Apresenta a documentação disponível para classe 'str'.
help(float)  # Apresenta a documentação disponível para classe 'float'.
help(list.append)  # Apresenta a documentação disponível para o método 'append' dentro da classe 'list'
