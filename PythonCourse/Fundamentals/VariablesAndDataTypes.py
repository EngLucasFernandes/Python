# Tipos de dados básicos em Python:

# int
# float
# str
# bool

age = 18  # Usando o tipo 'int'
price = 9.99  # Usando o tipo 'float'
name = "Lucas"  # Usando o tipo 'str'
is_monday = False  # Usando o tipo 'bool'

# Usando a função type() para saber o tipo de uma variável:

print(type(age))
print(type(price))
print(type(name))
print(type(is_monday))

# Dica: É possível representar números inteiros grandes separando-os as casas com '_'

# Exemplo:

# 1_000_000_000_000 - Representação de 'um trilhão' usando '_'
# 1000000000000 - Representação de 'um trilhão' sem usar '_'
