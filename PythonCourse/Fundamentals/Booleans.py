# Uma variável do tipo 'bool' representa um dos valores lógicos possíveis: Verdadeiro (True) ou Falso (False)

# Exemplo:

user_connected = False

# Operações básicas:

# Negação: Invertemos um valor lógico usando 'not'

# Conjunção: Usamos 'and'

# True and True -> True
# True and False -> False
# False and True -> False
# False and False -> False

# Disjunção: Usamos 'or'

# True or True -> True
# True or False -> True
# False or True -> True
# False or False -> False

# Exemplo:

user_logged = True
not_logged = not user_logged  # Negando 'user_logged' (O resultado é 'False')

# Operações com operadores relacionais (>, <, >=, <=, ==, !=) retornam 'True' ou 'False'

# Exemplos:

true_or_false = 5 == 3
print(true_or_false)
