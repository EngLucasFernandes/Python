# A função input() permite a entrada de dados em um programa Python.

# Exemplo:

name = input("Digite o seu nome: ")  # Entrada de dados
print(f"Seja bem-vindo(a), {name}!")  # Saída de dados

# Sintaxe:

# input(<prompt>)

# OBS: '<prompt>' será exibido na tela antes da entrada de dados.
# OBS: A função input() retorna por padrão uma string.
