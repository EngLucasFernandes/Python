# Números inteiros

# Exemplos de números inteiros: 0, -44, 25, -28, 7

# Exemplos de números que NÃO são inteiros: 4/5, 3.14, -7/9

# É possível converter um número NÃO inteiro ou uma string (se possível) em um número inteiro usando a função int()

# Exemplo:

pi = 3.14
integer_pi = int(pi)

print(integer_pi)
